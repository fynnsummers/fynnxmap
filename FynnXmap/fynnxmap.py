import socket
import subprocess
import re

blue = "\033[34m"
reset = "\033[0m"
green = "\033[32m"

def get_ipv4_address():
    hostname = socket.gethostname()
    ip_address = socket.gethostbyname(hostname)
    return ip_address

def get_wifi_ssid():
    try:
        # For Windows
        result = subprocess.check_output(['netsh', 'wlan', 'show', 'interfaces'])
        result = result.decode('cp850', errors='ignore')
        ssid_match = re.search(r'SSID\s*:\s*(.*)', result)
        if ssid_match:
            return ssid_match.group(1).strip()
        else:
            return "SSID nicht gefunden"
        
    except subprocess.CalledProcessError:
        return "Fehler beim Abrufen der SSID"
    except Exception as e:
        return f"Fehler: {str(e)}"

def list_connected_devices():
    # This depends on the router and its capabilities
    print(f"    {blue}Nach Ger√§ten suchen...")

