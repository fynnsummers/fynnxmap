import asyncio
from bleak import BleakScanner

blue = "\033[34m"
reset = "\033[0m"
red = "\033[31m"
green = "\033[32m"
yellow = "\033[33m"

async def scan_ble_devices():
    print("\n    Warte...")
    scanner = BleakScanner()
    devices = await scanner.discover(timeout=10)
    
    if devices:
        for device in devices:
            name = device.name if device.name else f"{red}Unbekannt"
            print(f"    {blue}Adresse: {green}{device.address}, {blue}Name: {green}{name}{yellow}")
    else:
        print("\n    Keine Ger√§te gefunden.")

if __name__ == "__main__":
    asyncio.run(scan_ble_devices())

